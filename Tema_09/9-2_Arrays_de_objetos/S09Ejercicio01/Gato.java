/**
 * Gato.java
 */

public class Gato {

    private String nombre;
    private String raza;
    private String sexo;
    private int edad;

    public String getNombre() {
        return this.nombre;
    }
    public void setNombre(String n) {
        this.nombre = n;
    }

    public String getRaza() {
        return this.raza;
    }
    public void setRaza(String r) {
        this.raza = r;
    }

    public String getSexo() {
        return this.sexo;
    }
    public void setSexo(String x) {
        this.sexo = x;
    }

    public int getEdad() {
        return this.edad;
    }
    public void setEdad(int e) {
        this.edad = e;
    }
}